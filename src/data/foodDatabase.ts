// Search function
export function searchFoods(query: string, category?: FoodCategory): FoodItem[] {
  const normalizedQuery = query.toLowerCase().trim();

  if (!normalizedQuery && !category) {
    return FOOD_DATABASE.slice(0, 20);
  }

  return FOOD_DATABASE.filter((food) => {
    const matchesCategory = !category || food.category === category;
    const matchesQuery = !normalizedQuery ||
      food.name.toLowerCase().includes(normalizedQuery) ||
      food.tags.some(tag => tag.toLowerCase().includes(normalizedQuery)) ||
      food.category.toLowerCase().includes(normalizedQuery);

    return matchesCategory && matchesQuery;
  });
}

// Get foods by category
export function getFoodsByCategory(category: FoodCategory): FoodItem[] {
  return FOOD_DATABASE.filter((food) => food.category === category);
}

// Calculate nutrition for custom portion
export function calculateNutrition(food: FoodItem, portionMultiplier: number) {
  return {
    calories: Math.round(food.calories * portionMultiplier),
    protein: Math.round(food.protein * portionMultiplier * 10) / 10,
    carbs: Math.round(food.carbs * portionMultiplier * 10) / 10,
    fat: Math.round(food.fat * portionMultiplier * 10) / 10,
    fiber: Math.round(food.fiber * portionMultiplier * 10) / 10,
  };
}

// Get food by ID
export function getFoodById(id: string): FoodItem | undefined {
  return FOOD_DATABASE.find((food) => food.id === id);
}
