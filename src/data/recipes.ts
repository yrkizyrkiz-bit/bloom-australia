// Helper function to get recipes by meal type
export function getRecipesByMealType(mealType: string): Recipe[] {
  if (mealType === "all") return RECIPES;
  return RECIPES.filter(r => r.mealType === mealType);
}

// Helper function to get recipes by dietary tag
export function getRecipesByDietaryTag(tag: string): Recipe[] {
  return RECIPES.filter(r => r.dietaryTags.includes(tag));
}

// Helper function to search recipes
export function searchRecipes(query: string): Recipe[] {
  const lowerQuery = query.toLowerCase();
  return RECIPES.filter(r =>
    r.title.toLowerCase().includes(lowerQuery) ||
    r.description.toLowerCase().includes(lowerQuery) ||
    r.dietaryTags.some(t => t.toLowerCase().includes(lowerQuery))
  );
}

// Get featured recipes (random selection)
export function getFeaturedRecipes(count: number = 6): Recipe[] {
  const shuffled = [...RECIPES].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}
