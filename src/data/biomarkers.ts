export function getBiomarkerById(id: string): BiomarkerDefinition | undefined {
  return biomarkerDefinitions.find((b) => b.id === id);
}

export function getBiomarkersByCategory(category: BiomarkerCategory): BiomarkerDefinition[] {
  return biomarkerDefinitions.filter((b) => b.category === category);
}

export function getStatusForValue(
  biomarker: BiomarkerDefinition,
  value: number,
  gender: "male" | "female"
): "optimal" | "normal" | "out_of_range" | "critical" {
  const range = biomarker.ranges[gender];

  if (value >= range.optimal_low && value <= range.optimal_high) {
    return "optimal";
  }
  if (value >= range.low && value <= range.high) {
    return "normal";
  }
  if (value < range.low * 0.5 || value > range.high * 1.5) {
    return "critical";
  }
  return "out_of_range";
}
